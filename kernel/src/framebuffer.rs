use limine::framebuffer::Framebuffer;

#[derive(Clone, Copy)]
pub struct Color {
    pub r: u8,
    pub g: u8,
    pub b: u8,
}

impl Color {
    pub const fn from_hex(hex: u32) -> Self {
        Self {
            r: ((hex >> 16) & 0xFF) as u8,
            g: ((hex >> 8)  & 0xFF) as u8,
            b: (hex & 0xFF) as u8,
        }
    }
}

pub struct Display {
    addr:   *mut u8,
    width:  usize,
    height: usize,
    pitch:  usize,
    bpp:    usize,
    r_shift: u8,
    g_shift: u8,
    b_shift: u8,
}

unsafe impl Send for Display {}

impl Display {
    pub fn new(fb: &Framebuffer) -> Self {
        Self {
            addr:     fb.address() as *mut u8,
            width:    fb.width as usize,
            height:   fb.height as usize,
            pitch:    fb.pitch as usize,
            bpp:      fb.bpp as usize / 8,
            r_shift:  fb.red_mask_shift,
            g_shift:  fb.green_mask_shift,
            b_shift:  fb.blue_mask_shift,
        }
    }

    pub fn width(&self)  -> usize { self.width  }
    pub fn height(&self) -> usize { self.height }

    #[inline]
    pub fn put_pixel_pub(&mut self, x: usize, y: usize, c: Color) {
        self.put_pixel(x, y, c);
    }

    #[inline]
    fn put_pixel(&mut self, x: usize, y: usize, c: Color) {
        if x >= self.width || y >= self.height { return; }
        let offset = y * self.pitch + x * self.bpp;
        let pixel: u32 = ((c.r as u32) << self.r_shift)
                       | ((c.g as u32) << self.g_shift)
                       | ((c.b as u32) << self.b_shift);
        unsafe {
            let px = self.addr.add(offset) as *mut u32;
            *px = pixel;
        }
    }

    pub fn clear(&mut self, c: Color) {
        for y in 0..self.height {
            for x in 0..self.width {
                self.put_pixel(x, y, c);
            }
        }
    }

    pub fn fill_rect(&mut self, x: usize, y: usize, w: usize, h: usize, c: Color) {
        for row in y..y.saturating_add(h) {
            for col in x..x.saturating_add(w) {
                self.put_pixel(col, row, c);
            }
        }
    }

    pub fn draw_text(&mut self, x: usize, y: usize, text: &str, c: Color, scale: usize) {
        let mut cx = x;
        for ch in text.chars() {
            let glyph = FONT.get(ch as usize).unwrap_or(&FONT[b'?' as usize]);
            for (row, &bits) in glyph.iter().enumerate() {
                for col in 0..8usize {
                    if bits & (1 << col) != 0 {
                        self.fill_rect(
                            cx + col * scale,
                            y + row * scale,
                            scale,
                            scale,
                            c,
                        );
                    }
                }
            }
            cx += 9 * scale;
        }
    }
}

